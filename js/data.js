// Парсим текущую страницу
const items = [];

document.querySelectorAll('.product-card').forEach((item, index) => {
  const imageUrl = item.querySelector('.catalog-card-img')?.src;
  
  // Пропускаем товары с заглушкой
  if (imageUrl && !imageUrl.includes('blank.png')) {
    const data = {
      id: Date.now() + index, // Уникальный ID
      title: item.querySelector('[itemprop="name"]')?.textContent.trim(),
      description: item.querySelector('[itemprop="description"]')?.content,
      image: imageUrl,
      link: 'https://mysakura.ru' + item.querySelector('a.image-place')?.getAttribute('href'),
      price: item.querySelector('[itemprop="price"]')?.content,
      car: item.querySelectorAll('.product-spec-value')[0]?.textContent.trim(),
      body: item.querySelectorAll('.product-spec-value')[1]?.textContent.trim(),
      engine: item.querySelectorAll('.product-spec-value')[2]?.textContent.trim(),
      oem: item.querySelectorAll('.product-spec-value')[3]?.textContent.trim(),
      condition: item.querySelectorAll('.product-spec-value')[4]?.textContent.trim(),
      photosCount: item.querySelector('.badge-photos')?.textContent.trim(),
      isNew: !!item.querySelector('.badge-new')
    };
    items.push(data);
  }
});

